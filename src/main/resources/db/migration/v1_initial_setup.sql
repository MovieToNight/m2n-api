create table movie_cart (
       id bigint not null,
        actors varchar(255),
        date varchar(255),
        description varchar(255),
        genre varchar(255),
        imdb_rating varchar(255),
        title varchar(255),
        our_rating float,
        runtime varchar(255),
        movie_type varchar(255),
        poster_url varchar(255),
        primary key (id)
    )
